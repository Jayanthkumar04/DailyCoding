package PATTERNS;

public class ABCD {

	public static void main(String[] args) {

		for(int i=1;i<=4;i++)
		{
			for(int j=1;j<=4;j++)
			{
				System.out.print((char)(64+j)+" ");
			}
			
			System.out.println();
		}
		
		System.out.println();
		

		System.out.println();
		
		for(int i=1;i<=4;i++)
		{
			for(int j=1;j<=4;j++)
			{
				System.out.print((char)(96+j)+" ");
			}
			
			System.out.println();
		}
		
	}

}
